package it.uniroma3.siw.rentalev.payload.request;

public class SwapRequest {
	
	private Long id_hub;
	private Long id_scooter;
	private Long id_battery;
	
	
	public Long getId_hub() {
		return id_hub;
	}
	public void setId_hub(Long id_hub) {
		this.id_hub = id_hub;
	}
	public Long getId_scooter() {
		return id_scooter;
	}
	public void setId_scooter(Long id_scooter) {
		this.id_scooter = id_scooter;
	}
	public Long getId_battery() {
		return id_battery;
	}
	public void setId_battery(Long id_battery) {
		this.id_battery = id_battery;
	}
	
	
	
	
	
}
